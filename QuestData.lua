local function collectQuestIDs(data, exp)
    local ids = {}
    -- Achievements
    if data.achievements then
        for _, ach in pairs(data.achievements) do
            if ach.quest then
                for _, q in pairs(ach.quest) do
                    if q.wqID then
                        ids[q.wqID] = true
                    end
                end
            end
        end
    end
    -- Mounts
    if data.mounts then
        for _, mount in pairs(data.mounts) do
            if mount.quest then
                for _, q in pairs(mount.quest) do
                    if q.wqID then
                        ids[q.wqID] = true
                    end
                end
            elseif mount.questID then
                ids[mount.questID] = true
            end
        end
    end
    -- Pets
    if data.pets then
        for _, pet in pairs(data.pets) do
            if pet.quest then
                for _, q in pairs(pet.quest) do
                    if q.wqID then
                        ids[q.wqID] = true
                    end
                end
            elseif pet.questID then
                ids[pet.questID] = true
            end
        end
    end
    -- Toys
    if data.toys then
        for _, toy in pairs(data.toys) do
            if toy.quest then
                for _, q in pairs(toy.quest) do
                    if q.wqID then
                        ids[q.wqID] = true
                    end
                end
            elseif toy.questID then
                ids[toy.questID] = true
            end
        end
    end
    -- Miscellaneous
    if data.miscellaneous then
        for _, misc in pairs(data.miscellaneous) do
            if misc.quest then
                for _, q in pairs(misc.quest) do
                    if q.wqID then
                        ids[q.wqID] = true
                    end
                end
            elseif misc.questID then
                ids[misc.questID] = true
            end
        end
    end

    -- Append static untied IDs for this exp
    if staticUntiedIDs[exp] then
        for _, id in ipairs(staticUntiedIDs[exp]) do
            ids[id] = true
        end
    end

    -- Convert to array
    local array = {}
    for id in pairs(ids) do
        table.insert(array, id)
    end
    return array
end

-- Build for each expansion (7=Legion, 8=BfA, 9=Shadowlands, 10=Dragonflight, 11=WarWithin, 12=Midnight)
for exp = 7, 12 do
    local data = WQA.data[exp]
    if data then
        WQA.AllWorldQuestIDs[exp] = collectQuestIDs(data, exp)
    else
        WQA.AllWorldQuestIDs[exp] = staticUntiedIDs[exp] or {} -- Use static if no data
    end
end
